package exercicio3;

public class OperacaoMatematica {
    
    
    
    
    void PositivoOuNegativo(int i){
        if(i >0){
            System.out.println("Numero " + i + "eh positivo");
        }else{
            System.out.println("Numero " + i + "eh positivo");
        }
    }
    
    void ParOuImpar(int i){
        if(i%2==0){
            System.out.println("Numero " + i + " eh par");
        }else{
            System.out.println("Numero " + i + " eh impar");
        }
    }
}
